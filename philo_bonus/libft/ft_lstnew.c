#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*node;

	node = malloc(sizeof(t_list));
	if (!node)
		return (node);
	node ->content = content;
	node ->next = (t_list *)(void *)0;
	return (node);
}
